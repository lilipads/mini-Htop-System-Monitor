vector<Process>& System::Processes() {
  /*  Return a vector composed of the system's processes. */

  std::vector<int> pids = LinuxParser::Pids();
  processes_ = {};
  processes_.reserve(pids.size());
  for (int pid : pids) processes_.emplace_back(Process(pid));

  return processes_;
}

